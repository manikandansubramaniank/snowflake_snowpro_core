-- create new database

CREATE DATABASE IF NOT EXISTS SF_SNOWPRO_DB;

USE DATABASE SF_SNOWPRO_DB;

--create new schema

CREATE SCHEMA IF NOT EXISTS SF_SNOWPRO_SCH;

USE SCHEMA SF_SNOWPRO_SCH;

-- create two tables for loading data

CREATE TEMPORARY TABLE IF NOT EXISTS SF_SNOWPRO_DB.SF_SNOWPRO_SCH.SF_SNOWPRO_CORE_DUMPS_RAW (RAW_FILE VARIANT);

CREATE TABLE IF NOT EXISTS SF_SNOWPRO_DB.SF_SNOWPRO_SCH.SF_SNOWPRO_CORE_DUMPS
(ID	NUMBER(38,0),
TOPIC	VARCHAR(100),
SUB_TOPIC	VARCHAR(100),
QUESTION	VARCHAR(2000),
CHOICES	OBJECT,
ANSWER	ARRAY,
EXPLANATION	VARCHAR(20000));

-- create stage

CREATE STAGE IF NOT EXISTS SF_SNOWPRO_DB.SF_SNOWPRO_SCH.SF_SNOWPRO_CORE_STAGE
FILE_FORMAT = (TYPE = 'JSON');

-- Using snowsql CLI Upload the file which is in data folder

PUT file:///media/manibhavani/Mani_Bhavani/Snowflake/sh_scripts/data/input/SF_SNOWPRO_CORE_DUMPS.json @SF_SNOWPRO_DB.SF_SNOWPRO_SCH.SF_SNOWPRO_CORE_STAGE
AUTO_COMPRESS = TRUE
OVERWRITE = TRUE
SOURCE_COMPRESSION = NONE;

-- check the file uploaded correctly

LIST @SF_SNOWPRO_DB.SF_SNOWPRO_SCH.SF_SNOWPRO_CORE_STAGE;

-- Load into the temp table using copy command

COPY INTO SF_SNOWPRO_DB.SF_SNOWPRO_SCH.SF_SNOWPRO_CORE_DUMPS_RAW
FROM @SF_SNOWPRO_DB.SF_SNOWPRO_SCH.SF_SNOWPRO_CORE_STAGE/SF_SNOWPRO_CORE_DUMPS.json.gz;


SELECT * FROM SF_SNOWPRO_DB.SF_SNOWPRO_SCH.SF_SNOWPRO_CORE_DUMPS_RAW;

-- Insert into SF_SNOWPRO_DB.SF_SNOWPRO_SCH.SF_SNOWPRO_CORE_DUMPS Table from Temp table

INSERT INTO SF_SNOWPRO_DB.SF_SNOWPRO_SCH.SF_SNOWPRO_CORE_DUMPS
(SELECT FL_Q_N_A.VALUE:"id"::NUMBER "ID",
Q_N_A."TOPIC",
Q_N_A."SUB_TOPIC",
FL_Q_N_A.VALUE:"question"::VARCHAR "QUESTION",
FL_Q_N_A.VALUE:"choices"::OBJECT "CHOICES",
FL_Q_N_A.VALUE:"answer"::ARRAY "ANSWER",
FL_Q_N_A.VALUE:"explanation"::VARCHAR "EXPLANATION"
FROM
(SELECT FL_TOPIC."TOPIC",
FL_SUBTOPIC.VALUE:"sub topic":: VARCHAR "SUB_TOPIC",
FL_SUBTOPIC.VALUE:"sub topic contents"."questions and answers"::VARIANT "questions and answers"
FROM
(SELECT FL_DATA.VALUE:"topic"::VARCHAR "TOPIC",
FL_DATA.VALUE:"topic contents"::VARIANT "topic contents"
FROM
(SELECT RAW_FILE:"data"::VARIANT "data" FROM SF_SNOWPRO_DB.SF_SNOWPRO_SCH.SF_SNOWPRO_CORE_DUMPS_RAW) RAW,
LATERAL FLATTEN (INPUT => "data", OUTER => TRUE) FL_DATA) FL_TOPIC,
LATERAL FLATTEN (INPUT => "topic contents", OUTER => TRUE) FL_SUBTOPIC) Q_N_A,
LATERAL FLATTEN (INPUT => "questions and answers", OUTER => TRUE) FL_Q_N_A
ORDER BY FL_Q_N_A.VALUE:"id");

SELECT * FROM SF_SNOWPRO_DB.SF_SNOWPRO_SCH.SF_SNOWPRO_CORE_DUMPS;

